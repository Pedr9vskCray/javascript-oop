// similar to python, javascript is a dynamically-typed language

// use the keyword "let" to declare variables 

let name = "pedro";
let age = 20;
let num1 = 10;
let num2 = 15;
let media = (num1 + num2)/2;

// use the keyword "const" to declare constants 

const birthYear = "21/02/2005";
const height = 1.86;

// use console.log() to print outputs in the console

console.log(media);
console.log(name);
console.log(birthYear);

// to check the type of a variable you can use typeof()

console.log(typeof(birthYear)); // type -> string

// you can change variables values with using keyword let after they have been defined

let value = 10;
value = 15

console.log(value)